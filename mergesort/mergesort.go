func merge1(m []int) []int {
	if len(m) <= 1 {
		return m
	}
	mid := len(m) / 2
	return merge2(merge1(m[:mid]), merge1(m[mid:]))
}

func merge2(left []int, right []int) []int {
	list := []int{}
	for len(left) > 0 && len(right) > 0 {
		if left[0] < right[0] {
			list = append(list, left[0])
			left = left[1:]
		} else {
			list = append(list, right[0])
			right = right[1:]
		}
	}
	if len(left) > 0 {
		list = append(list, left...)
	}
	if len(right) > 0 {
		list = append(list, right...)
	}
	return list
}

